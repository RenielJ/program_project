# Subscripting
print("Hello" [-2])

# String
print("123" + "345") # Concatenated

# Integer = Whole number
print(123 + 345)

# Large Integers
print(123_345_456) # 123,345,567 interpreted in Human but in computer used "_"

# Float = Floating Point Number
print(3.14159)

# Boolean
print(True)
print(False)